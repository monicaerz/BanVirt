    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(this.jTextField1.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,
            "Por favor, indique el número de la cuenta",
            "Numero de cuenta",
            JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            if(this.jTextField2.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this,
                "Por favor, indique el monto a ingresar",
                "Monto",
                JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                try{
                    int numCuenta = Integer.parseInt(this.jTextField1.getText());
                    try{
                        int monto = Integer.parseInt(this.jTextField2.getText());
                        if(monto <= 0)
                        {
                            JOptionPane.showMessageDialog(this,
                            "El monto a ingresar debe ser mayor a 0",
                            "Monto inválido",
                            JOptionPane.ERROR_MESSAGE);
                            return;
                        }
                        String tipoCuenta;
                        if(this.jRadioButton1.isSelected())
                        {
                            tipoCuenta = "ahorros";
                        }
                        else
                        {
                            tipoCuenta = "corriente";
                        }
                        try {
                            this.conexionCliente.ingresoCuenta(idTransaccion, idCliente, tipoCuenta, numCuenta, monto);
                            JOptionPane.showMessageDialog(this,
                            "Se hizo una consignación de "+monto+" en la cuenta "+numCuenta,
                            "Ingreso exitoso",
                            JOptionPane.INFORMATION_MESSAGE);
                        } catch (RemoteException ex) {
                            System.out.println("Error al ingresar en cuenta: "+ex);
                        }
                    }catch(NumberFormatException e){
                        JOptionPane.showMessageDialog(this,
                        "El monto a ingresar debe ser un valor numérico",
                        "Monto inválido",
                        JOptionPane.ERROR_MESSAGE);
                    }
                }catch(NumberFormatException e)
                    {
                        JOptionPane.showMessageDialog(this,
                        "El número de cuenta debe ser un valor numérico",
                        "Numero de cuenta inválido",
                        JOptionPane.ERROR_MESSAGE);
                    }
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

